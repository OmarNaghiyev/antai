import type { SpeciesRecord } from "@/lib/types";

function scientificNameToSlug(scientificName: string) {
  return scientificName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

